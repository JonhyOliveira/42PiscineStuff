#include <unistd.h>

void	rush(int x, int y)
{
	int		a;
	int		b;
	char	c;

	a = 1;
	b = 1;
	while (b <= y)
	{
		while (a <= x)
		{
			if (a == 1 && b == 1 || a == x && b == 1 || a == 1 && b == y \
			|| a == y && b == y)
				write (1, &"o", 1);
			if (b == 1 && a > 1 && a < x || b == y && a > 1 && a < x)
				write (1, &"-", 1);
			if (a == 1 && b > 1 && b < y || a == x && b > 1 && b < y)
				write (1, &"|", 1);
			if (a > 1 && a < y && b > 1 && b < y)
				write (1, &" ", 1);
			a++;
		}
		b++;
		a = 1;
		if (x > 0)
			write (1, &"\n", 1);
	}
}
